package fi.margokomarova.calculator.util;

public class Calculator {
    public static int addNumbers(int a, int b) {
        int result = a + b;
        return result;
    }

    public static int subNumbers(int a, int b) {
        int result = a - b;
        return result;
    }

    public static int multNumbers(int a, int b) {
        int result = a * b;
        return result;
    }
}
